def seed_db(conn: sqlite3.Connection) -> None:
    """Insert demo permits into an open connection if the table is empty."""
    count = conn.execute("SELECT COUNT(*) FROM permits").fetchone()[0]
    if count > 0:
        return
    cur = conn.cursor()
    for p in PERMITS:
        cur.execute(_INSERT_SQL, p)


def insert_permits():
    conn = sqlite3.connect(DB_PATH)
    cur = conn.cursor()

    inserted = 0
    skipped = 0
    for p in PERMITS:
        existing = cur.execute(
            "SELECT id FROM permits WHERE genehmigungsnummer = ?",
            (p["genehmigungsnummer"],)
        ).fetchone()
        if existing:
            skipped += 1
            continue

        cur.execute(_INSERT_SQL, p)
        inserted += 1

    conn.commit()
    conn.close()
    print(f"Fertig: {inserted} Genehmigungen eingefügt, {skipped} bereits vorhanden.")


if __name__ == "__main__":
    insert_permits()
